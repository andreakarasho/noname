            io.BackendFlags |= ImGuiBackendFlags_HasMouseCursors | ImGuiBackendFlags_HasSetMousePos;

            io.GetClipboardTextFn.Pointer = &GetClipboardText;
            io.SetClipboardTextFn.Pointer = &SetClipboardText;

