            var pipeLineDesc = new Gfx.PipelineDesc();
            pipeLineDesc.Layout.Buffers[0].Stride = sizeof(ImGui.ImDrawVert);
            ref var attrs = ref pipeLineDesc.Layout.Attrs;
            attrs[0].Offset = 0;
